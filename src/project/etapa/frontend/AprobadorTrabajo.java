    private void estadoBotones(boolean estado){
        buttonAprobar.setEnabled(estado);
        buttonRechazar.setEnabled(estado);
    }
    
    private void actualizarLista(){
        ManejadorEtapa me = new ManejadorEtapa(DB);
        listaEtapasObs.clear();
        if(me.getEtapaComentarioNotNull(usuario) != null){
            listaEtapasObs.addAll(me.getEtapaComentarioNotNull(usuario));
        }
    }

