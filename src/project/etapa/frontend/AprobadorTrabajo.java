    private void buttonAprobarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonAprobarActionPerformed
        ManejadorEtapa me = new ManejadorEtapa(DB);
        ManejadorCaso mc = new ManejadorCaso(DB);
        try {
            me.aprobarEtapa(etapaSelecc);
            mc.updateCasoDateAvance(etapaSelecc, formattedTextFieldFecha.getText());
            JOptionPane.showMessageDialog(rootPane, "Se ha aprobado exitosamente el trabajo seleccionado", "Accion exitosa", JOptionPane.INFORMATION_MESSAGE);
            actualizarLista();
        } catch (Exception e) {
            e.printStackTrace(System.out);
        }
        estadoBotones(false);
    }//GEN-LAST:event_buttonAprobarActionPerformed

