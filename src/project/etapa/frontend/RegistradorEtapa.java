    private void buttonRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonRegistrarActionPerformed
        ManejadorCaso mc = new ManejadorCaso(this.DB);
        ManejadorEtapa me = new ManejadorEtapa(this.DB);
        String itemSelecc = (String) jComboBoxIDCaso.getSelectedItem();
        if (itemSelecc.replaceAll(" ", "").isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "No cuenta con casos asignados, intentelo en otra oportunidad");
        } else {
            this.tipoCaso = mc.getTipoCasoByCasoId(itemSelecc);
            if (me.getEtapaByCasoAndPaso("1", itemSelecc) == null) {
                try {
                    me.setEtapaNoPasoIdCaso(tipoCaso.getNumeroPasos(), itemSelecc);
                    JOptionPane.showMessageDialog(rootPane, "Se han registrado exitosamente las etapas del Caso \""
                            + itemSelecc + "\"", "Accion exitosa", JOptionPane.INFORMATION_MESSAGE);
                } catch (Exception e) {
                    JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error de validacion", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(rootPane, "No es posible registrar las etapas, pues ya han sido creadas anteriormente",
                        "Error de validacion", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_buttonRegistrarActionPerformed

