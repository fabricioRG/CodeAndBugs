    private void actualizarListas() {
        ManejadorCaso mc = new ManejadorCaso(this.DB);
        listaCasos.clear();
        if (mc.getCasoByStatusAndDpiAdmin(this.usr) != null) {
            listaCasos.addAll(mc.getCasoByStatusAndDpiAdmin(this.usr));
            for (Caso caso : listaCasos) {
                jComboBoxIDCaso.addItem(Integer.toString(caso.getID()));
            }
        } else {
            jComboBoxIDCaso.addItem("");
        }
    }

