    private void buttonModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonModificarActionPerformed
        ManejadorCaso mc = new ManejadorCaso(DB);
        try {
            mc.updateDateCaso(casoSelecc, formattedTextFieldFechaLim.getText());
            JOptionPane.showMessageDialog(rootPane, "Se ha modificado exitosamente la fecha del Caso con ID \"" + casoSelecc.getID() + "\"", "Accion exitosa", JOptionPane.INFORMATION_MESSAGE);
            actualizarLista();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error de validacion", JOptionPane.ERROR_MESSAGE);
        }
        modificarBotones(false);
    }//GEN-LAST:event_buttonModificarActionPerformed

    private void buttonCancelarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_buttonCancelarActionPerformed
        String respuesta = JOptionPane.showInputDialog(rootPane, "Motivo de la Cancelacion (Max: 100 caracteres)");
        ManejadorCaso mc =new ManejadorCaso(DB);
        if(respuesta != null){
            try {
                mc.cancelCaso(casoSelecc, respuesta);
                actualizarLista();
            } catch (Exception e) {
                JOptionPane.showMessageDialog(rootPane, e.getMessage(), "Error de validacion", JOptionPane.ERROR_MESSAGE);
                e.printStackTrace(System.out);
            }
        }
        modificarBotones(false);
    }//GEN-LAST:event_buttonCancelarActionPerformed

    private void jTable1MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable1MouseClicked
        String fechaSelected = fechaFormat.format(casoSelecc.getFechaLimite());
        formattedTextFieldFechaLim.setText(fechaSelected.replaceAll("-", ""));
        modificarBotones(true);
    }//GEN-LAST:event_jTable1MouseClicked

