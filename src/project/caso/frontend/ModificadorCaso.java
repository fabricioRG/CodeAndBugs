    private void actualizarLista() {
        ManejadorCaso mc = new ManejadorCaso(this.DB);
        listaCasosObser.clear();
        if (mc.getCasoByStatusAndDpiAdmin(this.usuario) != null) {
            listaCasosObser.addAll(mc.getCasoByStatusAndDpiAdmin(this.usuario));
        }
    }

    private void modificarBotones(boolean estado){
        buttonCancelar.setEnabled(estado);
        buttonModificar.setEnabled(estado);
    }
    
